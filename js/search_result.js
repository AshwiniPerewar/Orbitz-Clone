function init() 
{
    const eleContainer = document.querySelector('#container');
    for(let j = 0;j < 20; j += 1)
    {
      let ran = Math.floor(Math.random() * 10);
      const trip_card = document.createElement('div');
      trip_card.setAttribute('class','trip-card');
      trip_card.setAttribute('id',`trip-card-${j}`);
      
      const trip_slider = document.createElement('div');
      trip_slider.setAttribute('class','trip-slider');
      
      const wrapper = document.createElement('div');
      wrapper.setAttribute('class','wrapper');
      
      const prev_btn = document.createElement('div');
      prev_btn.setAttribute('class','prev-btn');
      const next_btn = document.createElement('div');
      next_btn.setAttribute('class','next-btn');

      const left_arrow = document.createElement('i');
      left_arrow.setAttribute('class','fas fa-chevron-left');
      const right_arrow = document.createElement('i');
      right_arrow.setAttribute('class','fas fa-chevron-right');
      prev_btn.append(left_arrow);
      next_btn.append(right_arrow);

      const slides_container = document.createElement('div');
      slides_container.setAttribute('class','slides-container');

      const slide_image1 = document.createElement('div');
      slide_image1.setAttribute('class','slide-image');
      const image_1 = document.createElement('img');
      image_1.src = data[ran].slide_image_1;
      slide_image1.append(image_1);

      const slide_image2 = document.createElement('div');
      slide_image2.setAttribute('class','slide-image');
      const image_2 = document.createElement('img');
      image_2.src = data[ran].slide_image_2;
      slide_image2.append(image_2);

      const slide_image3 = document.createElement('div');
      slide_image3.setAttribute('class','slide-image');
      const image_3 = document.createElement('img');
      image_3.src = data[ran].slide_image_3;
      slide_image3.append(image_3);
      
      slides_container.append(slide_image1, slide_image2, slide_image3);
      wrapper.append(prev_btn, next_btn,slides_container);
      trip_slider.append(wrapper);

      const trip_data = document.createElement('div');
      trip_data.setAttribute('class','trip-data');
      const trip_main = document.createElement('div');
      trip_main.setAttribute('class','trip-main');

      const trip_main_p1 = document.createElement('p');
      trip_main_p1.innerText = data[ran].trip_main_1;
      const trip_main_p2 = document.createElement('p');
      trip_main_p2.innerText = data[ran].trip_main_2;
      trip_main.append(trip_main_p1, trip_main_p2);

      const trip_essential = document.createElement('div');
      trip_essential.setAttribute('class','trip-essential');
      const trip_review = document.createElement('div');
      trip_review.setAttribute('class','trip-review');
      const trip_review_p1 = document.createElement('p');
      trip_review_p1.innerText = data[ran].trip_review_1;
      const trip_review_p2 = document.createElement('p');
      trip_review_p2.innerText = data[ran].trip_review_2;
      const trip_review_p3 = document.createElement('p');
      trip_review_p3.innerText = data[ran].trip_review_3;
      const trip_review_p4 = document.createElement('p');
      trip_review_p4.innerHTML = `<span>${data[ran].trip_review_span}</span>` + data[ran].trip_review_4;
      
      const trip_discount = document.createElement('div');
      trip_discount.setAttribute('class','trip-discount');
      const trip_discount_off = document.createElement('span');
      trip_discount_off.innerText = data[ran].trip_discount_span;
      const trip_discount_p1 = document.createElement('p');
      trip_discount_p1.innerHTML = `<span>${data[ran].trip_discount_inner_span}</span>` + data[ran].trip_discount_p;
      const trip_discount_p2 = document.createElement('p');
      trip_discount_p2.innerText = data[ran].trip_discount_total;
      trip_discount.append(trip_discount_off,trip_discount_p1,trip_discount_p2);
      
      trip_review.append(trip_review_p1,trip_review_p2,trip_review_p3, trip_review_p4)
      trip_essential.append(trip_review, trip_discount);
      trip_data.append(trip_main, trip_essential);
      trip_card.append(trip_slider, trip_data);
      eleContainer.append(trip_card);

      const slideImage = document.querySelectorAll(`#trip-card-${j} .slide-image`);
      const slidesContainer = document.querySelector(`#trip-card-${j} .slides-container`);
      const nextBtn = document.querySelector(`#trip-card-${j} .next-btn`);
      const prevBtn = document.querySelector(`#trip-card-${j} .prev-btn`);

      let numberOfImages = slideImage.length;
      let slideWidth = slideImage[0].clientWidth;
      let currentSlide = 0; 
      
      function goToSlide(slideNumber) {
        slidesContainer.style.transform =
          "translateX(-" + slideWidth * slideNumber + "px)";
      
        currentSlide = slideNumber;
        setActiveClass();
      }
      
      function setActiveClass() 
      {
        let currentActive = document.querySelector(".slide-image.active");
        currentActive.classList.remove("active");
        slideImage[currentSlide].classList.add("active");
      }

      nextBtn.addEventListener("click", () => {
        if (currentSlide >= numberOfImages - 1) {
          goToSlide(0);
          return;
        }
      
        currentSlide++;
        goToSlide(currentSlide);
      });

      prevBtn.addEventListener("click", () => {
        if (currentSlide <= 0) {
          goToSlide(numberOfImages - 1);
          return;
        }
      
        currentSlide--;
        goToSlide(currentSlide);
      });
      
      slideImage.forEach((img, i) => {
         img.style.left = i * 100 + "%";
       });

      slideImage[0].classList.add("active");
    
    }
}

init();

let tripData = document.querySelectorAll('.trip-data');
for(let i = 0; i < tripData.length;i += 1)
{
  tripData[i].addEventListener('click',function(event){
    let hotelName = tripData[i].childNodes[0].childNodes[0].innerText;
    let hotelLocation = tripData[i].childNodes[0].childNodes[1].innerText; 
    let images_ = tripData[i].parentElement.childNodes[0].childNodes[0].childNodes[2].childNodes;
    let images__ = []
    let dataToShow = {};
    for(let j = 0; j < images_.length; j ++)
    {
      images__.push(images_[j].childNodes[0].src);
    }
    
    dataToShow['hotelName'] = hotelName;
    dataToShow['hotelLocation'] = hotelLocation;
    dataToShow['hotelImages'] = images__;

    
    localStorage.setItem('data', JSON.stringify(dataToShow));
    window.location.href = 'booking.html';
  });
}